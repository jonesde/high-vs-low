def parse_evaluation_report(report_text):
    """Extract count_hl, count_ll, and score from an evaluation report."""
    # Try to get counts from the Scoring Summary table
    hl_match = re.search(
        r"High Law Aligned\s+\|?\s+(\d+)", report_text
    )
    ll_match = re.search(
        r"Low Law Aligned\s+\|?\s+(\d+)", report_text
    )

    # Also try header counts as fallback
    if not hl_match:
        hl_match = re.search(
            r"### High Law Aligned \((\d+) statements?\)", report_text
        )
    if not ll_match:
        ll_match = re.search(
            r"### Low Law Aligned \((\d+) statements?\)", report_text
        )

    count_hl = int(hl_match.group(1)) if hl_match else 0
    count_ll = int(ll_match.group(1)) if ll_match else 0

    # Try to get score from the report
    score = None
    score_match = re.search(
        r"\*\*Score\*\*.*?=\s*\*\*([+-]?\d+\.?\d*)\*\*", report_text
    )
    if score_match:
        score = float(score_match.group(1))
    else:
        # Calculate from counts
        total = count_hl + count_ll
        if total > 0:
            hl_pct = 100 * count_hl / total
            ll_pct = 100 * count_ll / total
            score = round((hl_pct - ll_pct) / 10, 1)

    return count_hl, count_ll, score


def parse_review_result(review_text):
    """Extract updated counts from a review result."""
    # Try "Updated counts" line
    updated_match = re.search(
        r"Updated counts.*?HL\s*=\s*(\d+).*?LL\s*=\s*(\d+).*?Score\s*=\s*([+-]?\d+\.?\d*)",
        review_text,
        re.DOTALL,
    )
    if updated_match:
        return (
            int(updated_match.group(1)),
            int(updated_match.group(2)),
            float(updated_match.group(3)),
        )

    # Fallback: try to parse from the full review text
    hl_match = re.search(r"HL\s*=\s*(\d+)", review_text)
    ll_match = re.search(r"LL\s*=\s*(\d+)", review_text)
    score_match = re.search(r"Score\s*=\s*([+-]?\d+\.?\d*)", review_text)

    if hl_match and ll_match and score_match:
        return (
            int(hl_match.group(1)),
            int(ll_match.group(1)),
            float(score_match.group(1)),
        )

    return None, None, None


